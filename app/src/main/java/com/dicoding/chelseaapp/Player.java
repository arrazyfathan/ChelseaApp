package com.dicoding.chelseaapp;

public class Player {
    private String name;
    private String position;
    private String shortPosition;
    private String noPemain;
    private String age;
    private String height;
    private String weight;
    private String biography;
    private int avatar;
    private int photo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getNoPemain() {
        return noPemain;
    }

    public void setNoPemain(String noPemain) {
        this.noPemain = noPemain;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getBiography() {
        return biography;
    }

    public void setBiography(String biography) {
        this.biography = biography;
    }

    public int getAvatar() {
        return avatar;
    }

    public void setAvatar(int avatar) {
        this.avatar = avatar;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }


    public String getShortPosition() {
        return shortPosition;
    }

    public void setShortPosition(String shortPosition) {
        this.shortPosition = shortPosition;
    }
}
