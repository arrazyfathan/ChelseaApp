package com.dicoding.chelseaapp;

public class Trophy {
    private String nameTrophy;
    private int imgThrophy;
    private String totalTrophy;

    public String getNameTrophy() {
        return nameTrophy;
    }

    public void setNameTrophy(String nameTrophy) {
        this.nameTrophy = nameTrophy;
    }

    public int getImgThrophy() {
        return imgThrophy;
    }

    public void setImgThrophy(int imgThrophy) {
        this.imgThrophy = imgThrophy;
    }

    public String getTotalTrophy() {
        return totalTrophy;
    }

    public void setTotalTrophy(String totalTrophy) {
        this.totalTrophy = totalTrophy;
    }
}
